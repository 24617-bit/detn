function translatePage() {
    const lang = localStorage.getItem('lang') || 'fr';
    document.documentElement.lang = lang;
    
    // Set text direction
    if (lang === 'ar') {
        document.documentElement.dir = 'rtl';
        document.documentElement.classList.add('rtl');
    } else {
        document.documentElement.dir = 'ltr';
        document.documentElement.classList.remove('rtl');
    }

    // Translate all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Update language segment active states
    const btnFr = document.getElementById('lang-fr');
    const btnAr = document.getElementById('lang-ar');
    if (btnFr && btnAr) {
        if (lang === 'fr') {
            btnFr.className = "flex h-7 items-center justify-center rounded bg-background shadow-sm px-3 text-xs font-bold text-foreground transition-colors";
            btnAr.className = "flex h-7 items-center justify-center rounded px-3 text-xs font-bold text-muted-foreground hover:text-foreground transition-colors";
        } else {
            btnAr.className = "flex h-7 items-center justify-center rounded bg-background shadow-sm px-3 text-xs font-bold text-foreground transition-colors";
            btnFr.className = "flex h-7 items-center justify-center rounded px-3 text-xs font-bold text-muted-foreground hover:text-foreground transition-colors";
        }
    }

    // Dispatch event to notify other scripts that language changed
    window.dispatchEvent(new Event('langChanged'));
}

// Global initialization
window.translations = translations;
window.translatePage = translatePage;

document.addEventListener('DOMContentLoaded', () => {
    translatePage();
});
